		GameRegistry.addSmelting(ModBlocks.ORE_OVERWORLD, new ItemStack(ModItems.COPPER_INGOT, 1), 2.0f);
		GameRegistry.addSmelting(ModBlocks.ORE_OVERWORLD, new ItemStack(ModItems.TIN_INGOT, 1), 2.0f);
